def normalize_word(word: str) -> str:
    word_lower = word.lower()
    if len(word_lower) < 3 or word_lower in STOP_WORDS:
        return word_lower
    if word_lower in EXACT_NORMALIZATION:
        return EXACT_NORMALIZATION[word_lower]
    if re.search(r'[a-z]', word_lower):
        return word_lower
    endings = (
        'ая', 'яя', 'ие', 'ые', 'ое', 'ее', 'ого', 'его', 'ому', 'ему',
        'ым', 'им', 'ом', 'ем', 'ых', 'их', 'ую', 'юю', 'ой', 'ей',
        'ам', 'ям', 'ах', 'ях', 'ов', 'ев', 'ей', 'ям', 'ями', 'ях',
        'ь', 'и', 'ы', 'у', 'ю', 'е', 'ё', 'о', 'а', 'я', 'й'
    )
    for end in endings:
        if word_lower.endswith(end):
            return word_lower[:-len(end)]
    return word_lower

def normalize_keywords(keywords: list) -> list:
    seen = set()
    result = []
    for kw in keywords:
        norm = normalize_word(kw)
        if norm not in seen:
            seen.add(norm)
            result.append(norm)
    return result